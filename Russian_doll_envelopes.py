        # vector to hold max number of envelopes to russian doll for each envelope
        self.dp = [1] * len(envelopes)

        # vector used to calculate max number of envelopes. We allocate the maximum possible
        # memory this vector might use
        self.ranking = [2 ** 31] * (len(envelopes) + 1)

        for i, envelope in enumerate(envelopes):
            pos = self.bisect(envelope[1])
            self.dp[i] = pos
            self.ranking[pos] = envelope[1]

        return max(self.dp)

